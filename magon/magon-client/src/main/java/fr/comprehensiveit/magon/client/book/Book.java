package fr.comprehensiveit.magon.client.book;

public class Book {

	public int id;
	public String title;
	
	/**
	 * Get id
	 * @return int
	 */
	public int getId() {
		return id;
	}
	
	/**
	 * Set id
	 * @param id
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	/**
	 * Get title
	 * @return String
	 */
	public String getTitle() {
		return title;
	}
	
	/**
	 * Set title
	 * @param title
	 */
	public void setTitle(String title) {
		this.title = title;
	}
}
